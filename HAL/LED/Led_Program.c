/******************************************************************************
* \Syntax          : void Led_OpType(uint8 port_num, uint8 pin_num, boolean level)                                      
* \Description     : Configure the LED operation                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : port_num - LED port
                     pin_num - LED pin
                     level - Operation level (HIGH/LOW)                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/
